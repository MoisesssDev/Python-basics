# Crie uma tupla com apenas nomes (strings) e uma lista com apenas números.
# Depois imprima: o maior e menor elemento da tupla e o maior e o menor elemento da lista.

tupla = ('saulo', 'andré', 'jose', 'maria')
lista = [12, 70, -9, 15, -9.2, 70.2]

print ('Maior e menor da tupla: ', max(tupla), min(tupla))
print ('Maior e menor da lista: ', max(lista), min(lista))